#Sale de un dado el premio por completar la misión de un juego
#1-Gana un cofre/2-Gana una gema/3-Gana una vara/4-Ganó el premio mayor/5-no gana nada

valor = 5

if valor == 1:
    print("Ganó un Cofre")
elif valor == 2:
    print("Ganó una Gema")
elif valor == 3:
    print("Ganó una Vara") 
elif valor == 4:
    print("Ganó el premio mayor")
elif valor == 5:
    print("No ganó nada")
else:
    print("Valor inválido")